from .base_supplier import BaseSupplier


class RowenSupplier(BaseSupplier):
    """
    Парсер счетов поставщика РОВЕН-Оренбург.
    Особенности:
    - Позиции сгруппированы по системам (П1, В1, ВЕ1, ПД1 и т.д.)
    - Нужна колонка "Цена со скидкой (с НДС)" — не обычная цена
    - Системы могут быть любого формата: П1, В1, ВЕ1-ВЕ14, ПД1 и др.
    """

    @property
    def supplier_name(self) -> str:
        return "РОВЕН-Оренбург"

    @property
    def system_prompt(self) -> str:
        return """Ты парсер счетов от поставщика РОВЕН-Оренбург.

Твоя задача: извлечь все позиции из счёта, сгруппировав их по системам вентиляции.

ПРАВИЛА:
1. Системы обозначаются как П1, П2, В1, В2, ВЕ1, ВЕ2, ПД1 и т.д. — это заголовки групп в таблице счёта
2. Для каждой позиции бери колонку "Цена со скидкой (с НДС)" — это поле price_rub
3. Если у позиции несколько штук — qty указывай как есть, price_rub = цена за 1 штуку
4. Включай ВСЕ позиции — оборудование, решётки, заслонки, виброизоляторы, материалы и т.д.
5. Если позиция относится к нескольким системам (например "ВЕ8,9*") — записывай как одну систему с таким названием

Верни ТОЛЬКО JSON без пояснений и без markdown-обёртки:
[
  {
    "system": "П1",
    "items": [
      {
        "name": "Полное название товара из счёта",
        "qty": 1,
        "unit": "шт",
        "price_rub": 551035.08
      }
    ]
  },
  {
    "system": "В1",
    "items": [...]
  }
]"""
